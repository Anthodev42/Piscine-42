int	ft_iterative_factorial(int nb)
{
	int	i;
	int	nb2;

	if (nb < 0)
	{
		return (0);
	}	
	i = 1;
	nb2 = nb;
	if ((nb == 1) || (nb == 0))
	{
		return (1);
	}
	while (nb2 > i)
	{
		nb *= i;
		i++;
	}
	return (nb);
}
