int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb < 2)
	{
		return (0);
	}
	while (nb > i)
	{	
		if (nb % i == 0)
		{
			return (0);
		}
		i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	int	n;

	n = nb;
	if (nb <= 2)
		return (2);
	while (ft_is_prime(n) != 1)
	{
		n++;
	}
	return (n);
}
